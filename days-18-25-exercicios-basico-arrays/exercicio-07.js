// 7 - Adicione um novo elemento ao início do array.

// 1º passo - Criar o array
let meuArray = [10, 20, 30, 40, 50];

// 2º passo - Exibir o array criado
console.log('Array:', meuArray);

// 3º passo - Adicionar um elemento no início
